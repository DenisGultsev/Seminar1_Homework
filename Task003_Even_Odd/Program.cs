Console.Write("Enter number: ");
int number = Convert.ToInt32(Console.ReadLine());
if (number % 2 == 0)
{
    Console.WriteLine(number + " → yes");
}
else
{
    Console.WriteLine(number + " → no");
}

/*
Описание кода:

1. `Console.Write("Введите число: ");` - эта строка кода выводит сообщение "Введите число: " в консоль. Она использует команду `Console.Write`, чтобы вывести текст без перевода строки на следующую строку, чтобы пользователь мог ввести число на той же строке, что и сообщение.

2. `int number = Convert.ToInt32(Console.ReadLine());` - эта строка кода запрашивает число у пользователя, считывает его ввод, используя команду `Console.ReadLine()`` и преобразует это значение в `int` с помощью метода `Convert.ToInt32()`. Значение присваивается переменной `number`, которая в дальнейшем будет использоваться в проверке является ли число четным.

3. `if (number % 2 == 0) { ... } else { ... }` - эта строка кода содержит оператор `if`, который выполняет проверку, является ли число, введенное пользователем, четным, используя оператор остатка `%` (который возвращает остаток от деления `number` на `2`). Если `number` делится на 2 без остатка, то это число четное и программа выводит "yes" в конце числа, иначе программа выводит "no".

4. `Console.WriteLine(number + " → yes");` - если условие в `if` верно (число является четным), то программа выводит сообщение в консоль, используя команду `Console.WriteLine()`, добавив знак "→" как разделитель между числом и "yes".

5. `Console.WriteLine(number + " → no"); ` – если условие в `if` ложно (число не является четным), то программа выводит в консоль сообщение с помощью команды `Console.WriteLine()`, добавив знак "→" как разделитель между числом и "no".

Таким образом, данная программа на C# позволяет пользователю определить, является ли число четным, и вывести результат в консоль в виде сообщения "num → yes", если число четное, или "num → no", если число нечетное.
*/