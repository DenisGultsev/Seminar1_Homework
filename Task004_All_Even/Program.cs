Console.Write("Enter number: ");
int number = Convert.ToInt32(Console.ReadLine());

Console.Write("{0} → ", number);
for (int i = 2; i <= number; i += 2)
{
    Console.Write("{0} ", i);
}

Console.WriteLine(" ");

/*
Описание кода:

Код принимает целочисленное значение N от пользователя и выводит на экран все четные числа от 1 до N в формате "N → четные числа от 1 до N".

Вот описание работы кода по строкам:

1. `Console.Write("Enter number: ");`: выводит на экран сообщение "Enter number: ", чтобы пользователь знал, что нужно ввести число.
    
2. `int number = Convert.ToInt32(Console.ReadLine());`: принимает входное значение от пользователя (в виде строки), преобразует его в целое число и сохраняет его в переменной `number`.
    
3. `Console.Write("{0} → ", number);`: выводит на экран число `number` вместе с стрелкой "→", чтобы показать, что затем будут идти все четные числа от 1 до `number`.
    
4. `for (int i = 2; i <= number; i += 2)`: цикл for, который перебирает все четные числа от 2 до `number`.
    
5. `Console.Write("{0} ", i);`: выводит каждое четное число в цикле, разделенное пробелом.
    
6. `Console.WriteLine(" ");`: выводит перевод строки после окончания цикла, чтобы следующий вывод начинался с новой строки.
    

Итоговый результат работы программы будет представлен числом, которое пользователь ввел в строке, за которым следуют все четные числа от 1 до этого числа. Например, "10 → 2 4 6 8 10".
*/